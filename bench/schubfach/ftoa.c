/*
   For full details about this code see the following references:

   [1] Giulietti, "The Schubfach way to render doubles",
       https://drive.google.com/open?id=1luHhyQF9zKlM8yJ1nebU0OgVYhfC6CBN

   [2] IEEE Computer Society, "IEEE Standard for Floating-Point Arithmetic"

   [3] Bouvier & Zimmermann, "Division-Free Binary-to-Decimal Conversion"

   Divisions are avoided altogether for the benefit of those architectures
   that do not provide specific machine instructions or where they are slow.
   This is discussed in section 10 of [1].
*/

// The precision in bits.
static const int _P = 53;

// Exponent width in bits.
static const int _W = (64 - 1) - (_P - 1);

// Minimum value of the exponent: -(2^(W-1)) - P + 3.
static const int _Q_MIN = (-(1 << (_W - 1))) - _P + 3;

// Maximum value of the exponent: 2^(W-1) - P.
static const int _Q_MAX = (1 << (_W - 1)) - _P;

// 10^(E_MIN - 1) <= MIN_VALUE < 10^E_MIN
static const int _E_MIN = -323;

// 10^(E_MAX - 1) <= MAX_VALUE < 10^E_MAX
static const int _E_MAX = 309;

// Threshold to detect tiny values, as in section 8.1.1 of [1]
static const int _C_TINY = 3;

// The minimum and maximum k, as in section 8 of [1]
static const int _K_MIN = -324;
static const int _K_MAX = 292;

// H is as in section 8 of [1].
static const int _H = 17;

// Minimum value of the significand of a normal value: 2^(P-1).
static const uint64_t _C_MIN = (1ULL<<_P) - 1;

// Mask to extract the biased exponent.
static const int _BQ_MASK = (1 << _W) - 1;

// Mask to extract the fraction bits.
static const uint64_t _T_MASK = (1ULL << (_P - 1)) - 1;

// Used in rop().
static const uint64_t _MASK_63 = (1ULL << 63) - 1;

// Used for left-to-tight digit extraction.
static const uint64_t _MASK_28 = (1ULL << 28) - 1;

static uint64_t gg1(int k) {
	return g[(k-_K_MIN)<<1];
}

/**
 * Returns <i>g</i><sub>0</sub> as described in
 * {@link #g1(int)}.
 *
 * @param k The exponent of 10, which must meet
 *          {@link #K_MIN} &le; {@code e} &le; {@link #K_MAX}.
 * @return <i>g</i><sub>0</sub> as described in
 * {@link #g1(int)}.
 */
static uint64_t gg0(int k) {
	return g[(k-_K_MIN)<<1|1];
}

/*
Computes rop(cp g 2^(-127)), where g = g1 2^63 + g0
See section 9.10 and figure 5 of [1].
*/
static uint64_t rop(uint64_t g1, uint64_t g0, uint64_t cp) {
	unsigned __int128 full;
	full = (unsigned __int128)g0 * cp;
	uint64_t x1 = full >> 64;
	full = (unsigned __int128)g1*cp;
	uint64_t y0 = full;
	uint64_t y1 = full >> 64;
	uint64_t z = (y0 >> 1) + x1;
	uint64_t vbp = y1 + (((int64_t)z)>>63);
	return vbp | (((z&_MASK_63)+_MASK_63)>>63);
}

static void toDecimal(int q, uint64_t c, int dk, uint64_t *dp, int *pp) {
	/*
	   The skeleton corresponds to figure 4 of [1].
	   The efficient computations are those summarized in figure 7.

	   Here's a correspondence between Java names and names in [1],
	   expressed as approximate LaTeX source code and informally.
	   Other names are identical.
	   cb:     \bar{c}     "c-bar"
	   cbr:    \bar{c}_r   "c-bar-r"
	   cbl:    \bar{c}_l   "c-bar-l"

	   vb:     \bar{v}     "v-bar"
	   vbr:    \bar{v}_r   "v-bar-r"
	   vbl:    \bar{v}_l   "v-bar-l"

	   rop:    r_o'        "r-o-prime"
	*/

	int out = c&1;
	uint64_t cb = c<<2;
	uint64_t cbr = cb + 2;

	/*
	   flog10pow2(e) = floor(log_10(2^e))
	   flog10threeQuartersPow2(e) = floor(log_10(3/4 2^e))
	   flog2pow10(e) = floor(log_2(10^e))
	*/
	uint64_t cbl;
	int k;
	if (c != _C_MIN || q == _Q_MIN) {
		// regular spacing
		cbl = cb - 2;
		k = flog10pow2(q);
	} else {
		// irregular spacing
		cbl = cb - 1;
		k = flog10threeQuartersPow2(q);
	}
	uint64_t h = q + flog2pow10(-k) + 2;

	// g1 and g0 are as in section 9.9.3 of [1], so g = g1 2^63 + g0
	uint64_t g1 = gg1(k);
	uint64_t g0 = gg0(k);

	uint64_t vb = rop(g1, g0, cb<<h);
	uint64_t vbl = rop(g1, g0, cbl<<h);
	uint64_t vbr = rop(g1, g0, cbr<<h);

	uint64_t s = vb >> 2;
	if (s >= 100) {
		/*
		   For n = 17, m = 1 the table in section 10 of [1] shows
		       s' = floor(s / 10) = floor(s 115292150460684698 / 2^60)
		          = floor(s 115292150460684698 2^4 / 2^64)

		   sp10 = 10 s'
		   tp10 = 10 t'
		   upin    iff    u' = sp10 10^k in Rv
		   wpin    iff    w' = tp10 10^k in Rv
		   See section 9.4 of [1].
		*/
		h = ((unsigned __int128)s * (115292150460684698ULL<<4)) >> 64;
		uint64_t sp10 = 10 * h;
		uint64_t tp10 = sp10 + 10;
		int upin = vbl+out <= sp10<<2;
		int wpin = (tp10<<2)+out <= vbr;
		if (upin != wpin) {
			if (upin) {
				trimZeros(sp10, k, dp, pp);
				return;
			}
			trimZeros(tp10, k, dp, pp);
			return;
		}
	}

	/*
	   10 <= s < 100    or    s >= 100  and  u', w' not in Rv
	   uin    iff    u = s 10^k in Rv
	   win    iff    w = t 10^k in Rv
	   See section 9.4 of [1].
	*/
	uint64_t t = s + 1;
	int uin =  vbl+out <= s<<2;
	int win = (t<<2)+out <= vbr;
	if (uin != win) {
		// Exactly one of u or w lies in Rv.
		if (uin) {
			trimZeros(s, k+dk, dp, pp);
			return;
		}
		trimZeros(t, k+dk, dp, pp);
		return;
	}

	/*
	   Both u and w lie in Rv: determine the one closest to v.
	   See section 9.4 of [1].
	*/
	int64_t cmp = vb - (s + (t<<1));
	if (cmp < 0 || (cmp == 0 && (s&1) == 0)) {
		trimZeros(s, k+dk, dp, pp);
		return;
	}
	trimZeros(t, k+dk, dp, pp);
}

void schubfachRaw(uint64_t *dp, int *pp, double f) {
	/*
	   For full details see references [2] and [1].

	   For finite v != 0, determine integers c and q such that
	       |v| = c 2^q    and
	       Q_MIN <= q <= Q_MAX    and
	           either    2^(P-1) <= c < 2^P                 (normal)
	           or        0 < c < 2^(P-1)  and  q = Q_MIN    (subnormal)
	*/

	uint64_t bits;
	memmove(&bits, &f, sizeof bits);
	uint64_t t = bits & _T_MASK;
	int bq = (int)(bits >> (_P-1)) & _BQ_MASK;
	if (bq == _BQ_MASK)
		abort();
	if (bq != 0) {
		// normal value. Here mq = -q
		int mq = -_Q_MIN + 1 - bq;
		uint64_t c = _C_MIN | t;
		// The fast path discussed in section 8.2 of [1].
		if (0 < mq && mq < _P) {
			uint64_t f = c >> mq;
			if(f<<mq == c) {
				*dp = f;
				*pp = 0;
				return;
			}
		}
		toDecimal(-mq, c, 0, dp, pp);
		return;
	}

	// subnormal value
	if (t < _C_TINY) {
		toDecimal(_Q_MIN, 10*t, -1, dp, pp);
		return;
	}
	toDecimal(_Q_MIN, t, 0, dp, pp);
}
