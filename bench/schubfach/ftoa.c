static int flog10pow2(int e) {
	return ((e * _C_10) >> _Q_10);
}

static int flog10threeQuartersPow2(int e) {
	return ((e*_C_10 + _A_10) >> _Q_10);
}

static int flog2pow10(int e) {
	return ((e * _C_2) >> _Q_2);
}

